# Python Primitive Data Types

'''
Primitive data types in Python.
1. Strings
2. Integers
3. Floats
4. Booleans
'''

print("Hello"[2], "Hello"[-2])  # string splicing
print("123" + "345")  # numbers as strings and their concatenation

print(123 + 345)  # Integers

print(123_456_789)  # Large Integers. type() is still 'int'

print(3141.59) # Float

# Boolean
print(True)
print(False)

